const FALLBACK_HEX = '#9CA3AF';

export function getRalColor(code) {
  const normalized = String(code || '').trim();
  const popular = POPULAR_RAL_COLORS.find(c => c.code === normalized);
  if (popular) {
    return { code: normalized, hex: popular.hex, name: popular.name, found: true };
  }
  const entry = RAL_COLORS[normalized];
  if (entry) {
    return { code: normalized, hex: entry.hex, name: entry.name, found: true };
  }
  return { code: normalized, hex: FALLBACK_HEX, name: `RAL ${normalized || '—'}`, found: false };
}